function add(num1, num2){
    return num1 + num2;
}

function subtract(num1, num2){
    return num1 - num2;
}

function divide(num1, num2){
    return num1 / num2;
}

function multiply(num1, num2){
    return num1 * num2;
}

console.log(add(14,56));
console.log(subtract(5,3));
console.log(divide(5, 2));
console.log(multiply(5, 4));